
package projetofloricultura;

public class Funcionario extends Pessoa {
    
    private String cargo;
  
    public Funcionario(String cargo, String nome, String cpf, String data_nasc, String email, String celular, String rua, String cidade, String numero, String estado) {
        super(nome, cpf, data_nasc, email, celular, rua, cidade, numero, estado);
        this.cargo = cargo;
    }
  
    public String getCargo() {
        return cargo;
    }

    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getData_nasc() {
        return data_nasc;
    }

    public void setData_nasc(String data_nasc) {
        this.data_nasc = data_nasc;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getCelular() {
        return celular;
    }

    public void setCelular(String celular) {
        this.celular = celular;
    }

    public Endereco getEndereço() {
        return endereço;
    }

    public void setEndereço(Endereco endereço) {
        this.endereço = endereço;
    }
    
    
    
    
}